// üëá –Ω–∞—à —Ö—É–∫ –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è slug
const formatSlug: FieldHook = ({ value, data }) => {
  const source = (data?.title ?? value) as string | undefined
  if (!source) return value

  return source
    .trim()
    .toLowerCase()
    .replace(/[\s\W-]+/g, '-') // –≤—Å—ë, —á—Ç–æ –Ω–µ –±—É–∫–≤–∞/—Ü–∏—Ñ—Ä–∞, –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º –≤ –¥–µ—Ñ–∏—Å
    .replace(/^-+|-+$/g, '') // –æ–±—Ä–µ–∑–∞–µ–º –¥–µ—Ñ–∏—Å—ã –ø–æ –∫—Ä–∞—è–º
}

